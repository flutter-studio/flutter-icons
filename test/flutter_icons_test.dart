String toCamelName(String name) => name
    .split("_")
    .map((e) => "${e.substring(0, 1).toUpperCase()}${e.substring(1)}")
    .toList()
    .join("");

String toName(String name) {
  String _name = toCamelName(name);
  return "${_name.substring(0, 1).toLowerCase()}${_name.substring(1)}";
}

